export async function seedNews() {
  try {
    const existingNews = await storage.getNews();
    const existingTitles = new Set(existingNews?.map(n => n.title) || []);
    
    let addedCount = 0;
    for (const newsItem of newsItems) {
      if (!existingTitles.has(newsItem.title)) {
        await storage.createNews(newsItem);
        addedCount++;
      }
    }
    
    if (addedCount > 0) {
      console.log(`✓ Added ${addedCount} new news items`);
    } else {
      console.log("✓ All news items already exist");
    }
  } catch (error) {
    console.error("Error seeding news:", error);
  }
}
